import streamlit as st
import tensorflow as tf
import numpy as np
from PIL import Image
from tensorflow.keras.applications.resnet50 import preprocess_input

@st.cache_resource
def load_model():
    model = tf.keras.models.load_model("/content/best_skin_cancer_model.h5")
    return model

model = load_model()

class_labels = [
    'Melanocytic nevi (nv)',
    'Melanoma (mel)',
    'Benign keratosis-like lesions (bkl)',
    'Basal cell carcinoma (bcc)',
    'Actinic keratoses (akiec)',
    'Vascular lesions (vasc)',
    'Dermatofibroma (df)'
]

st.title("ğŸ”¬ Skin Cancer Classification")
st.write("upload the photo and i will detict the diagnose")

# upload the photo
uploaded_file = st.file_uploader("chose the photo....", type=["jpg", "jpeg", "png"])

if uploaded_file is not None:
    # Ø¹Ø±Ø¶ Ø§Ù„ØµÙˆØ±Ø©
    image = Image.open(uploaded_file).convert("RGB")
    st.image(image, caption="", use_container_width=True)

    # ØªØ¬Ù‡ÙŠØ² Ø§Ù„ØµÙˆØ±Ø© Ù„Ù„Ù…ÙˆØ¯ÙŠÙ„
    img = image.resize((224, 224))
    img_array = np.array(img)
    img_array = np.expand_dims(img_array, axis=0)  # Ø´ÙƒÙ„ (1, 224, 224, 3)
    img_array = preprocess_input(img_array)  # Ù†ÙØ³ Ù…Ø¹Ø§Ù„Ø¬Ø© ResNet50

    # Ø§Ù„ØªÙ†Ø¨Ø¤
    preds = model.predict(img_array)
    pred_class = np.argmax(preds, axis=1)[0]
